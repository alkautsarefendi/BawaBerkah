package org.bawaberkah.app.Model;

public class LogActivityModel {

    String aktivitas, tglAktivitas, waktuAktivitas;

    public String getAktivitas() {
        return aktivitas;
    }

    public void setAktivitas(String aktivitas) {
        this.aktivitas = aktivitas;
    }

    public String getTglAktivitas() {
        return tglAktivitas;
    }

    public void setTglAktivitas(String tglAktivitas) {
        this.tglAktivitas = tglAktivitas;
    }

    public String getWaktuAktivitas() {
        return waktuAktivitas;
    }

    public void setWaktuAktivitas(String waktuAktivitas) {
        this.waktuAktivitas = waktuAktivitas;
    }
}
