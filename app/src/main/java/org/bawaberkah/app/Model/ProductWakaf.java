package org.bawaberkah.app.Model;

public class ProductWakaf {

    private int percentage;
    private String judul, terkumpul, path, pathCampaign, start, end, id, campaigner, lokasi, jumDonatur;

    public int getPercentage() {
        return percentage;
    }

    public void setPercentage(int percentage) {
        this.percentage = percentage;
    }

    public String getJudul() {
        return judul;
    }

    public void setJudul(String judul) {
        this.judul = judul;
    }

    public String getTerkumpul() {
        return terkumpul;
    }

    public void setTerkumpul(String terkumpul) {
        this.terkumpul = terkumpul;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getPathCampaign() {
        return pathCampaign;
    }

    public void setPathCampaign(String pathCampaign) {
        this.pathCampaign = pathCampaign;
    }

    public String getStart() {
        return start;
    }

    public void setStart(String start) {
        this.start = start;
    }

    public String getEnd() {
        return end;
    }

    public void setEnd(String end) {
        this.end = end;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCampaigner() {
        return campaigner;
    }

    public void setCampaigner(String campaigner) {
        this.campaigner = campaigner;
    }

    public String getLokasi() {
        return lokasi;
    }

    public void setLokasi(String lokasi) {
        this.lokasi = lokasi;
    }

    public String getJumDonatur() {
        return jumDonatur;
    }

    public void setJumDonatur(String jumDonatur) {
        this.jumDonatur = jumDonatur;
    }

}
