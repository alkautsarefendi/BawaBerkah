package org.bawaberkah.app.Model;

public class InboxModel {

    String isi_berita, tglBerita, waktuBerita;

    public String getIsi_berita() {
        return isi_berita;
    }

    public void setIsi_berita(String isi_berita) {
        this.isi_berita = isi_berita;
    }

    public String getTglBerita() {
        return tglBerita;
    }

    public void setTglBerita(String tglBerita) {
        this.tglBerita = tglBerita;
    }

    public String getWaktuBerita() {
        return waktuBerita;
    }

    public void setWaktuBerita(String waktuBerita) {
        this.waktuBerita = waktuBerita;
    }
}
